package com.mvc.dto;

public class LargeDTO {
	private String largeCode;
	private String name;
	private String contentCode;
	private int largeIdx;
	
	public String getLargeCode() {
		return largeCode;
	}
	public void setLargeCode(String largeCode) {
		this.largeCode = largeCode;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getContentCode() {
		return contentCode;
	}
	public void setContentCode(String contentCode) {
		this.contentCode = contentCode;
	}
	public int getLargeIdx() {
		return largeIdx;
	}
	public void setLargeIdx(int largeIdx) {
		this.largeIdx = largeIdx;
	}
	
	
}
